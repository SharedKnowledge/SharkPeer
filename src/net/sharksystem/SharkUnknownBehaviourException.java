package net.sharksystem;

public class SharkUnknownBehaviourException extends Exception {
    public SharkUnknownBehaviourException() {
        super();
    }
    public SharkUnknownBehaviourException(String message) {
        super(message);
    }
    public SharkUnknownBehaviourException(String message, Throwable cause) {
        super(message, cause);
    }
    public SharkUnknownBehaviourException(Throwable cause) {
        super(cause);
    }
}
